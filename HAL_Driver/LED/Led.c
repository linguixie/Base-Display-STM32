/*******************************************************************************
* 函数名称:    LedInit
* 函数功能:    Led初始化
* 输入参数:    
* 输出参数:    无
* 返 回 值:    无
*******************************************************************************/
void LedInit(void)
{
}


/*******************************************************************************
* 函数名称:    LedOn
* 函数功能:    Led亮
* 输入参数:    Led的编号,参见枚举变量Led_Enum
* 输出参数:    无
* 返 回 值:    无
*******************************************************************************/
void LedOn(int LedIndex)
{
    switch(LedIndex)
    {
    case LED_SHUTLIMIT:    
        Led_ShutLimitClr();     
        break;
    case LED_OPENLIMIT:
        Led_OpenLimitClr();
        break;
    default:
        break;
    }
}


/*******************************************************************************
* 函数名称:    LedOff
* 函数功能:    Led灭
* 输入参数:    Led的编号,参见枚举变量Led_Enum
* 输出参数:    无
* 返 回 值:    无
*******************************************************************************/
void LedOff(int LedIndex)
{
    switch(LedIndex)
    {
    case LED_SHUTLIMIT:    
        Led_ShutLimitSet();     
        break;
    case LED_OPENLIMIT:
        Led_OpenLimitSet();
        break;
    default:
        break;
    }
}
 
